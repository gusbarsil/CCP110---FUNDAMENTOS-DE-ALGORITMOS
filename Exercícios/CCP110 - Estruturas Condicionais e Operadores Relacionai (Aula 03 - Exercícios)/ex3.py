n = int(input("Digite o número do dia da semana: "))

if n == 1:
    print(f"{n} é Domingo.")
    if n == 2:
        print(f"{n} é Segunda.")
        if n == 3:
            print(f"{n} é Terça.")
            if n == 4:
                print(f"{n} é Quarta.")
                if n == 5:
                    print(f"{n} é Quinta.")
                    if n == 6:
                        print(f"{n} é Sexta.")
                        if n == 7:
                            print(f"{n} é Sábado.")